''' Multiline comment with the help of single triples
1. A-Z        : Uppercase Alphabets
2. a-z        : Lowercase Alphabets
3. _          : Special chaacter i.e. Underscore _
4. 0-9        : Digits
5. A-Za-z     : Combination of uppercase and lowercase alphabets i.e A-Za-z
6. A-Za-z_    : Combination of uppercase, Lowercase and special character i.e. _
7. A-Za-z_0-9 : Conbination of uppercase, lowercase, special characteri.e _ and Digits i.e. 0-9
Note: A variable cannot be created with prefix as digit i.e. 0-9
'''

